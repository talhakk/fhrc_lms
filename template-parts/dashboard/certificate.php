<script>
    jQuery("#downloadPDF").click(function() {
    html2canvas(document.querySelector("#content2"), {
        logging: true,
        letterRendering: 1,
        useCORS: true,
        windowWidth: 1280, // Set the window width to match the print styles
        windowHeight: document.querySelector("#content2").scrollHeight,
        scale: 1,
        onclone: function(clonedDoc) {
            clonedDoc.getElementById('content2').style.width = '1100px';
             clonedDoc.getElementById('content2').style.boxShadow = 'none';
        }
    }).then(canvas => {
        var imgData = canvas.toDataURL('image/png');
        const { jsPDF } = window.jspdf;

        var pdf = new jsPDF({
            orientation: 'landscape',
            unit: 'px',
            format: [1100, 700] // You might need to adjust this
        });

        pdf.addImage(imgData, 'PNG', 0, 0, 1100, 700);
        pdf.save("azimuth-certificate.pdf");
    });
});

